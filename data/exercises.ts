// Helper functions for filtering exercises
export function getExercisesByMuscle(muscle: string): Exercise[] {
  return exercises.filter((exercise) => exercise.muscles.some((m) => m.toLowerCase().includes(muscle.toLowerCase())))
}

export function getExercisesByEquipment(equipment: string): Exercise[] {
  return exercises.filter((exercise) => exercise.equipment === equipment)
}

export function getExercisesByEnvironment(environment: "gimnasio" | "casa"): Exercise[] {
  return exercises.filter((exercise) => exercise.environment === environment)
}

export function getExercisesByMovement(movement: string): Exercise[] {
  return exercises.filter((exercise) => exercise.movement === movement)
}

export function searchExercises(query: string): Exercise[] {
  const lowercaseQuery = query.toLowerCase()
  return exercises.filter(
    (exercise) =>
      exercise.name.toLowerCase().includes(lowercaseQuery) ||
      exercise.muscles.some((muscle) => muscle.toLowerCase().includes(lowercaseQuery)) ||
      exercise.movement.toLowerCase().includes(lowercaseQuery) ||
      exercise.equipment.toLowerCase().includes(lowercaseQuery),
  )
}
